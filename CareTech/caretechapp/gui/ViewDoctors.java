    private void loadAllDoctorsDetails() {
        try{
            List<DoctorPojo>docList=DoctorsDao.getAllDoctorDetails();
            if(docList.isEmpty()){
                JOptionPane.showMessageDialog(null,"No Doctor registered!");
                return;
            }
            Object [] obj=new Object[7];
            for(DoctorPojo doc:docList){
                obj[0]=doc.getDoctorId();
                obj[1]=doc.getDoctorName();
                obj[2]=doc.getEmailId();
                obj[3]=doc.getContactNo();
                obj[4]=doc.getQualification();
                obj[5]=doc.getGender();
                obj[6]=doc.getSpecialist();
                 model.addRow(obj);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In View All  Doctor DB");
            ex.printStackTrace();
             
        }
    }
